def is_high_imdb_score(movie):
    """
    Checks if the IMDb score of a movie is above 5.5.

    Args:
        movie (dict): A dictionary containing movie details (name, imdb, category).

    Returns:
        bool: True if IMDb score is above 5.5, False otherwise.
    """
    return movie.get("imdb", 0) > 5.5

